# Задача 3
"""
Написать программу, которая выводит таблицу
умножения числа N в интервале от L до K. Ввод N, L,
K выполнять с клавиатуры.
"""

n = int(input("Введите N: "))
a = int(input("Введите L от 0 до 10: "))
b = int(input("Введите K от 0 до 10: "))
if 0 <= a <= 10 and 0 <= b <= 10 and a <= b:
    while a <= b:   # если L меньше K
        print(f"{n}*{a} = {n * a}")
        a += 1
elif 0 <= a <= 10 and 0 <= b <= 10 and a > b:
    while a >= b:   # если L больше K
        print(f"{n}*{a} = {n * a}")
        a -= 1
else:
    print("Ошибка условий")

