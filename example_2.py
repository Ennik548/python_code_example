# Задача 9
"""
Определить сложность пароля (сделать функцию как на обычных сайтах.
То есть проверять большие буквы, символы, цифры И так далее. Подсказка: ascii)
"""

# 1 - 2 флага = низкая сложность
# 3 - 4 флага = средняя сложность
# 5 флагов = высокая сложность

password = input("Введите пароль: ")
i = 0
flagLen = 0
flagNumber = 0
flagUpperCase = 0
flagLowerCase = 0
flagSpecial = 0
if len(password) < 6:
    print("Слишком короткий пароль. \
Пароль должен соделжать не менее 6 символов")
else:
    if len(password) >= 8:
        flagLen = 1
    while i < len(password):
        if 48 <= ord(password[i]) <= 57:  # Числа
            flagNumber = 1
        if 65 <= ord(password[i]) <= 90:  # Большие буквы
            flagUpperCase = 1
        if 97 <= ord(password[i]) <= 122:  # Маленькаие буквы
            flagLowerCase = 1
        if 33 <= ord(password[i]) <= 47 or 58 <= ord(password[i]) <= 64 or\
            91 <= ord(password[i]) <= 96 or\
                123 <= ord(password[i]) <= 126:  # Специальные символы
            flagLowerCase = 1
        i += 1
    res = flagLen + flagNumber + flagUpperCase + flagLowerCase + flagSpecial
    if res <= 2:
        rating = "Низкая сложность"
    elif 3 <= res <= 4:
        rating = "Средняя сложность"
    else:
        rating = "Высокая сложность"
    print(f"Сложность пароля: {res} балла - {rating}")

