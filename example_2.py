# Задача 7
"""
Необходимо написать программу, которая сможет посчитать повторяющиеся символы
и вывести сокращенную строку, пример:
Вход: s = 'aaaabbcaa'
Выход: 'a4b2c1a2'
"""

s = 'aaaabbcaa'
res = s[0]
i = 1
calc = 1
while i < len(s):
    if s[i - 1] == s[i]:
        calc += 1
    else:
        res += str(calc) + s[i]
        calc = 1
    i += 1
res += str(calc)
print(res)

